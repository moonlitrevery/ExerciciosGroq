def somar(a, b):
    return a + b

def multiplicar(a, b):
    return a * b

def dividir(a, b):
    return a / b

def subtrair(a, b):
    return a - b

def converter_celsius_para_fahrenheit(celsius):
    return (celsius * 9/5) + 32

def converter_fahrenheit_para_celsius(fahrenheit):
    return (fahrenheit - 32) * 5/9